// 模拟从外部API获取工作数据的函数
export const fetchJobsFromAPI = async (searchTerm = '', filters = []) => {
  // 模拟API调用延迟
  await new Promise(resolve => setTimeout(resolve, 1000));
  
  let filteredJobs = [...mockJobs];
  
  // 按搜索词过滤
  if (searchTerm) {
    filteredJobs = filteredJobs.filter(job => 
      job.title.toLowerCase().includes(searchTerm.toLowerCase()) ||
      job.company.toLowerCase().includes(searchTerm.toLowerCase()) ||
      job.skills.some(skill => skill.toLowerCase().includes(searchTerm.toLowerCase()))
    );
  }
  
  // 按筛选条件过滤
  if (filters.length > 0) {
    filteredJobs = filteredJobs.filter(job => {
      return filters.some(filter => {
        switch (filter) {
          case 'ux-designer':
            return job.title.toLowerCase().includes('ux') || job.title.toLowerCase().includes('designer');
          case 'ui-designer':
            return job.title.toLowerCase().includes('ui') || job.title.toLowerCase().includes('designer');
          case 'product-designer':
            return job.title.toLowerCase().includes('product') && job.title.toLowerCase().includes('designer');
          case 'visual-identity':
            return job.title.toLowerCase().includes('visual') || job.title.toLowerCase().includes('graphic');
          default:
            return false;
        }
      });
    });
  }
  
  return {
    jobs: filteredJobs,
    total: filteredJobs.length,
    page: 1,
    hasMore: false
  };
};

